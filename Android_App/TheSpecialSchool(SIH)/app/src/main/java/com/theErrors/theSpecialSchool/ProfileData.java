package com.theErrors.theSpecialSchool;

public class ProfileData {
    public String Name;
    public String Email;
    public int Class;
    public String Language;
    public String ProfileCreated;
    public String Disability;


    ProfileData(String n, String m, int c, String l, String p, String d) {
        Name = n;
        Email = m;
        Class = c;
        Language = l;
        ProfileCreated = p;
        Disability = d;
    }
}
